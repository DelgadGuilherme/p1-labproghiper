class Videos:
    def __init__(titulo,descricao,qntCurtida,qntVisualizacao):
        self._titulo = titulo
        self._descricao = descricao
        self._qntCurtida = qntCurtida
        self._qntVisualizacao = qntVisualizacao

    def get_titulo():
        return self._titulo

    def get_descricao():
        return self._descricao

    def get_qntCurtida():
        return self._qntCurtida

    def get_qntVisualizacao():
        return self._qntVisualizacao